package org.example;

public class Cliente {
    // Atributos da classe (variáveis).
    private String nome;
    private int idade;

    // Métodos da classe (funções).

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }
}
